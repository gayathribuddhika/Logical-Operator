public class LogicalOperator {

    
    public static void main(String[] args) {
        int sub1 = 45;
        int sub2 = 25;
        //&&--AND Operator,||--OR Operator
        if((sub1>=35) &&  (sub2>=35)){
            System.out.println("The condition is true");
        }
        else{
            System.out.println("The condition is false");
        }
}
}